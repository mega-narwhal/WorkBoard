    w("## 7. Real claude-mem run (sandboxed)")
    w("")
    w("We installed and ran claude-mem 13.6.1 for real (standalone node 22 + Bun + "
      "uv + Chroma + worker) in a throwaway `$HOME`, on the same frozen tiny "
      "transcripts. The pipeline ran end-to-end; the compression *model call* was "
      "blocked because the sandbox is intentionally not logged into Claude. It "
      "**validated**: one compression call per session over the full transcript; "
      "**no bulk/bootstrap mode** (per-session hooks only); compression on the "
      "**main subscription model** (not a cheap tier). The real `~/.claude` was "
      "never touched. Full detail: `REAL_RUN_FINDINGS.md`.")
    w("")

