    w("| WB recall index/detail tokens | **MEASURED** (real `card.py` vs frozen snapshot) |")
    w("| WB live write = 0 calls, nudge 306, digest 97 | **MEASURED** |")
    w("| claude-mem bootstrap structure (1 call/session, full transcript) | **MEASURED** via real run (§8) |")
